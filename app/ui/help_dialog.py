class HelpDialog(QDialog):
    def __init__(self, parent=None) -> None:
        super().__init__(parent)
        self.setWindowTitle("Справка")
        self.resize(920, 620)

        self.sections = QListWidget()
        self.sections.setMaximumWidth(230)
        self.content = QTextEdit()
        self.content.setReadOnly(True)

        for title, _html in HELP_SECTIONS:
            self.sections.addItem(QListWidgetItem(title))
        self.sections.currentRowChanged.connect(self._show_section)

        layout = QHBoxLayout()
        layout.addWidget(self.sections)
        layout.addWidget(self.content, 1)
        self.setLayout(layout)

        self.sections.setCurrentRow(0)

    def _show_section(self, index: int) -> None:
        if index < 0 or index >= len(HELP_SECTIONS):
            return
        _title, html = HELP_SECTIONS[index]
        self.content.setHtml(self._page(html))

    def _page(self, body: str) -> str:
        return f"""
        <html>
          <head>
            <style>
              body {{
                font-family: "Segoe UI", Arial, sans-serif;
                font-size: 14px;
                color: #1f2937;
                line-height: 1.5;
              }}
              h2 {{
                margin-top: 0;
                color: #0f172a;
              }}
              ul, ol {{
                padding-left: 22px;
              }}
              li {{
                margin: 5px 0;
              }}
            </style>
          </head>
          <body>{body}</body>
        </html>
        """
